package ejercicio1;

public class Operaciones1 {

	public static void main(String[] args) 
	{
		int numero1 = 15;
		int numero2 = 1;
		int numero3 = 6;
		int suma;
		int resta; 
		
		System.out.println("Bienvenido a nuestro programa.\nAquí le ayudaremos a calcular "
				+ "todas las operaciones que desee. ");
		
		suma = numero1 + numero2 + numero3;
		resta = suma - numero1;
		
		System.out.println("Resultado 1: " +suma);
		System.out.println("Resultado 2: " +resta);
		
	}

}

package ejemplo01;

public class Bolsa {
	
	int color;
	int tamano;
	double precio;
	String marca;
	boolean perfume;
	boolean asas;
	
	public Bolsa (int color, int tamano, double precio, String marca, boolean perfume, boolean asas) {
		this.color = color;
		this.tamano = tamano;
		this.precio = precio;
		this.marca = marca;
		this.perfume = perfume;
		this.asas = asas;
	}

}
